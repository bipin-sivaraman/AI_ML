#!/usr/bin/env python3
"""
CSC500-1: CT1 - Program to find the multiplication and division of two numbers.

"""
num1 = int(input('Enter the first number :'))
num2 = int(input('Enter the second number :'))
result_multiply = num1 * num2
result_division = num1 / num2 
print(f'{num1} * {num2} = {result_multiply}')
print(f'{num1} / {num2} = {result_division}')

